# Apply colour substitutions to CSS template once at module load.
def _build_css() -> str:
    css = CSS_TEMPLATE
    css = css.replace("ACCENT_COLOR", ACCENT)
    css = css.replace("TEXT_COLOR",   TEXT)
    css = css.replace("MUTED_COLOR",  MUTED)
    css = css.replace("BORDER_COLOR", BORDER)
    css = css.replace("GREEN_COLOR",  GREEN)
    css = css.replace("AMBER_COLOR",  AMBER)
    css = css.replace("RED_COLOR",    RED)
    # Footer goes through string substitution (not CSS variable) so it survives
    # WeasyPrint's strict CSS parser. The placeholder includes the unicode
    # bullet (·) directly inside double-quoted content.
    footer_css_safe = (
        FOOTER_TEXT
        .replace("\\", "\\\\")
        .replace('"', '\\"')
    )
    css = css.replace("FOOTER_PLACEHOLDER", footer_css_safe)
    return css


_CSS_CACHE: str | None = None
def _get_css() -> str:
    global _CSS_CACHE
    if _CSS_CACHE is None:
        _CSS_CACHE = _build_css()
    return _CSS_CACHE


# ── HTML rendering ────────────────────────────────────────────────────────────

def _markdown_to_html(md_text: str) -> str:
    """Convert markdown to HTML with table support. Always returns valid HTML."""
    import markdown as md
    return md.markdown(
        md_text,
        extensions=["tables", "fenced_code", "sane_lists", "toc"],
        output_format="html5",
    )


def _wrap_html(body_html: str, title: str) -> str:
    """Wrap rendered HTML in a full document with our CSS embedded."""
    css = _get_css()
    safe_title = html_lib.escape(title)
    return f"""<!doctype html>
<html lang="en">
<head>
<meta charset="utf-8">
<title>{safe_title}</title>
<style>{css}</style>
</head>
<body>
{body_html}
</body>
</html>"""


# ── Public API ────────────────────────────────────────────────────────────────

def _blocking_url_fetcher(url: str):
    """WeasyPrint url_fetcher that refuses ALL external/local resource loads.

    The audit PDF never needs to fetch images, stylesheets, or fonts by URL, so
    any fetch attempt originates from injected crawled HTML (file:// LFI or
    http:// SSRF). Raising here aborts that single resource load; WeasyPrint
    renders the document without it.
    """
    raise ValueError(f"external resource blocked in report PDF: {url[:80]!r}")


def render_pdf(markdown_text: str, output_path: Path, base_url: str) -> dict:
    """
    Render a markdown report to PDF with Aditya branding.

    Args:
        markdown_text: Markdown source (from server._build_report).
        output_path:   Where to write the PDF.
        base_url:      Audited URL — used in the page header (string-set on H1).

    Returns:
        {path: str, size_bytes: int, pages: int}

    Raises:
        ImportError if WeasyPrint or markdown is unavailable.
        OSError on filesystem failure.
    """
    # Import inside function so unit tests / dev environments without
    # weasyprint can still import this module.
    from weasyprint import HTML  # type: ignore

    if not markdown_text or not markdown_text.strip():
        raise ValueError("markdown_text is empty - cannot render PDF")

    body_html = _markdown_to_html(markdown_text)
    title = f"LibreCrawl Audit - {base_url}"
    full_html = _wrap_html(body_html, title)

    output_path = Path(output_path)
    output_path.parent.mkdir(parents=True, exist_ok=True)

    # SECURITY: the report body contains crawled, attacker-controlled text
    # (titles, metas, H1s). Markdown passes raw HTML through, so a crawled page
    # could embed <img src="file:///etc/passwd"> or <img src="http://127.0.0.1/">
    # and WeasyPrint's default fetcher would read that local file / hit that
    # internal service during render. The report legitimately needs NO external
    # resources, so we install a url_fetcher that refuses every fetch — this
    # closes both the local-file-read and SSRF vectors regardless of body HTML.
    HTML(string=full_html, base_url=str(output_path.parent),
         url_fetcher=_blocking_url_fetcher).write_pdf(target=str(output_path))

    # Page count - use pypdf to inspect the result. Failures fall back to 0.
    pages = 0
    try:
        from pypdf import PdfReader  # type: ignore
        with open(output_path, "rb") as f:
            reader = PdfReader(f)
            pages = len(reader.pages)
    except Exception:
        pages = 0

    size_bytes = output_path.stat().st_size

    return {
        "path":       str(output_path),
        "size_bytes": size_bytes,
        "pages":      pages,
        "generated":  datetime.utcnow().isoformat() + "Z",
    }
